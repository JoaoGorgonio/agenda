    private void lstContatoValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstContatoValueChanged
        if (lstContato.getSelectedIndex() != -1)
        {
            String[] n = new String[2];
            String tipoContato = "";
            n = lstContato.getSelectedValue().toString().split(" - ", 2);
            
            Classes.Agenda agenda = new Agenda();
            agenda.setNome(n[1].toString());
            lblNome.setText(n[0]);
            lblTelefone.setText(n[1]);
            lblTipoContato.setText(agenda.TipoContato());
            index = lstContato.getSelectedIndex();
            lstContato.setSelectedIndex(-1);
        }
        else
        {
            lblNome.setText(null);
            lblTelefone.setText(null);
            lblTipoContato.setText(null);
        }
    }//GEN-LAST:event_lstContatoValueChanged

    private void btnExcluir_ContatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluir_ContatoActionPerformed
        if (lstContato.getSelectedIndex() >= 0)
        {
            if (JOptionPane.showConfirmDialog(null, "Você deseja mesmo deletar o contato "+lblNome.getText(), "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE) == JOptionPane.YES_OPTION)
            {
                Classes.deletarContato deletar = new deletarContato();
                deletar.setParametro(lblNome.getText().toString());
                if (deletar.ValiDel())
                {
                    deletar.Deletar();
                    JOptionPane.showMessageDialog(null, "Contato deletado com sucesso.");
                    lista.removeElementAt(index);
                    lblNome.setText(null);
                    lblTelefone.setText(null);
                    lblTipoContato.setText(null);
                    lstContato.clearSelection();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Nome inválido!");;
                }
            }    
        }
        else
        {
            JOptionPane.showMessageDialog(null, "Selecione um contato da lista!");
        }
    }//GEN-LAST:event_btnExcluir_ContatoActionPerformed

