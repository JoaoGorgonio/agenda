    private void btnContato_AtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnContato_AtualizarActionPerformed
        Conexao con;
        
        try
        {
            con = new Conexao();
            con.Conectar();
            
            if (txtDDD_Atualizar.getText().equals("") || txtTelefone_Atualizar.getText().equals("") || rdbResidencial_Atualizar.isSelected() == false && rdbCelular_Atualizar.isSelected() == false)
            {
                JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            }
            else
            {
                boolean valiTel = false;
                char TipoTel = ' ';
                if (txtTelefone_Atualizar.getText().length() < 8)
                {
                    txtTelefone_Atualizar.setBackground(Color.red);
                }
                else
                {
                    txtTelefone_Atualizar.setBackground(Color.white);
                    valiTel = true;
                }
                
                if (valiTel == true)
                {
                    if (rdbResidencial_Atualizar.isSelected() == true)
                    {
                        TipoTel = 'R';
                    }
                    else if (rdbCelular_Atualizar.isSelected() == true)
                    {
                        TipoTel = 'M';
                    }
                    
                    con.Executar("UPDATE tb_contato SET cd_estado = "+txtDDD_Atualizar.getText()+", cd_telefone = '"+txtTelefone_Atualizar.getText()+"', ic_tipoTel = '"+TipoTel+"' WHERE nm_contato = '"+txtBuscar_Atualizar.getText()+"')");
                    JOptionPane.showMessageDialog(null, "Contato atualizado com sucesso.");
                    txtDDD_Atualizar.setText("");
                    txtTelefone_Atualizar.setText("");
                    rdbResidencial_Atualizar.setSelected(false);
                    rdbCelular_Atualizar.setSelected(false);
                    btnContato_Atualizar.setEnabled(false);
                }
            }
        }
        catch(ClassNotFoundException ex)
        {
            Logger.getLogger(Banco.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch(SQLException ex)
        {
            Logger.getLogger(Banco.class.getName()).log(Level.SEVERE, null, ex);
        }
        catch(NullPointerException ex)
        {
            Logger.getLogger(Banco.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnContato_AtualizarActionPerformed

